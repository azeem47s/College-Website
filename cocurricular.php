<p style="text-align: justify; font-size: 20pt">Each semester, first year students kick-start their extracurricular activities with Freshers’ Day”, a vibrant presentation of talent and spirit in cultural performances. It is also a stress free ice-breaker for new students and helps them form cultural links and sensible bonding on a multi cultural campus.</p>
